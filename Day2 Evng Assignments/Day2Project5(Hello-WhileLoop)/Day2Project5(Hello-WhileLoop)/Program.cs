using System;

// A C# Code to Print "Hello", five times.

namespace Day2Project5_Hello_WhileLoop_
{
    internal class Program
    {
        static void Main(string[] args)
        {
            //Variable Declaration Section
            int i = 1;

            //Program Logic Section & Printing Output Section

            while(i<=5)
            {
                Console.WriteLine("Hello");
                i++;
            }
        }
    }
}
